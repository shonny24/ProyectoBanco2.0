    /**
     * metodo que sirve para validar numeros y este es llamado cuando existe un
     * evento de teclado
     *
     * @param campo campo al cual se aplica la validacion
     * @param evt evento de teclado
     */
    public void validarNumeros(JTextField campo, KeyEvent evt) {
        char c = evt.getKeyChar();

        if (Character.isLetter(c)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("NO PUEDE INGRESAR LETRAS!!!"), java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("VALIDANDO DATOS"),
                    JOptionPane.INFORMATION_MESSAGE);
            campo.setCursor(null);
        } else if ((int) evt.getKeyChar() > 32 && (int) evt.getKeyChar() <= 47
                || (int) evt.getKeyChar() >= 58 && (int) evt.getKeyChar() <= 64
                || (int) evt.getKeyChar() >= 91 && (int) evt.getKeyChar() <= 96
                || (int) evt.getKeyChar() >= 123 && (int) evt.getKeyChar() <= 255) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("NO PUEDE INGRESAR SIMBOLOS!!!"), java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("VALIDANDO DATOS"),
                    JOptionPane.INFORMATION_MESSAGE);
            campo.setCursor(null);
        }
    }

