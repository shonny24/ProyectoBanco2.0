    private void jBagregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBagregarUsuarioActionPerformed
        // TODO add your handling code here:
        if (ValidarDatosAgregarJugador().equals("")) {
            String nombres = jTnombreUsuario.getText();
            String apellidos = jTapellidoUsuario.getText();
            String cedula = jTcedulaUsuario.getText();
            int edad = calcularEdad();
            String sexo = (String) jCsexoUsuario.getSelectedItem();
            String email = jTemailUsuario.getText();
            Date fechaCumpleanios = jDateFechaNacimiento.getCalendar().getTime();
            String usuario = jTusuarioCrearUsuario.getText();
            char[] obtenerPassword = jPasswordUsuario.getPassword();
            String password = "";
            for (int i = 0; i < obtenerPassword.length; i++) {
                password += obtenerPassword[i];
            }
            String preguntaSeguridad = (String) jTpreguntaCrearUsuario.getSelectedItem();
            String respuestaUsuario = jTrespuestaUsuario.getText();

            String permiso = (String) jComboBoxPermisoAdmin.getSelectedItem();
            Permiso permisoEnum = Permiso.valueOf(permiso);

            miCliente = new Cliente(nombres, apellidos, cedula, edad, sexo, email, fechaCumpleanios, usuario, password, preguntaSeguridad, respuestaUsuario, permisoEnum);

            //llamamos el metodo agregar cliente
            boolean agregarClient = miBanco.agregarCliente(miCliente);

            //Mostramos mensaje de que se agrego o no el Cliente
            if (agregarClient) {
                JOptionPane.showMessageDialog(null, java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("EL CLIENTE SE AGREGÓ"));
                setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("NO SE PUEDE AGREGAR EL CLIENTE"));
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, java.text.MessageFormat.format(java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("ERROR!!! "), new Object[]{ValidarDatosAgregarJugador()}), java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("VALIDANDO DATOS"),
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBagregarUsuarioActionPerformed

