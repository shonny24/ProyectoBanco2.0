    /**
     * metodo que sirve para validar que los campos no esten vacios
     *
     * @return un mensaje de error dependiendo si el campo esta vacio
     */
    public String ValidarDatosAgregarJugador() {//Metodo para comprobar que los datos esten completos
        String msj = "";
        if (jTnombreUsuario.getText().equals("")) {//Si jTnombreUsuario esta vacio
            msj += java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("POR FAVOR DIGITE EL NOMBRE.");
        }
        if (jTapellidoUsuario.getText().equals("")) {//Si jTapellidoUsuario esta vacio
            msj += java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("POR FAVOR DIGITE EL APELLIDO.");
        }
        if (jTcedulaUsuario.getText().equals("")) {//Si jTcedulaUsuarioesta vacio
            msj += java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("POR FAVOR DIGITE LA CEDULA.");
        }
        if (jTemailUsuario.getText().equals("")) {//Si jTemailUsuario esta vacio
            msj += java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("POR FAVOR DIGITE EL EMAIL.");
        }
        if (jDateFechaNacimiento.getCalendar() == null) {//Si jDateFechaNacimiento esta vacio
            msj += java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("POR FAVOR SELECCIONE UNA FECHA.");
        }
        if (jTusuarioCrearUsuario.getText().equals("")) {//Si jTusuarioCrearUsuario esta vacio
            msj += java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("POR FAVOR DIGITE EL USUARIO.");
        }

        if (jPasswordUsuario.getPassword().length == 0) {//Si jPasswordUsuario esta vacio
            msj += java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("POR FAVOR DIGITE UNA CONTRASEÑA");
        }
        if (jTrespuestaUsuario.getText().equals("")) {//Si jTrespuestaUsuario esta vacio
            msj += java.util.ResourceBundle.getBundle("properties/VentanaCrearCliente").getString("POR FAVOR DIGITE UNA RESPUESTA");
        }
        return msj;//devuelve msj
    }

