    public void inicializarCampos() {
        if (ahorro) {
            miAhorro = miCliente.buscarCuentaAhorro(idCuenta);
            jTNumeroCuenta.setText(miAhorro.getNumeroBancario());
            jTMonto.setText(miAhorro.getMonto() + "");
            jDateFechaRetiro.setDate(miAhorro.getFechaRetiroAhorros());

        } else if (corriente) {
            miCorriente = miCliente.buscarCuentaCorriente(idCuenta);
            jTNumeroCuenta.setText(miCorriente.getNumeroBancario());
            jTMonto.setText(miCorriente.getMonto() + "");
            
        } else {
            miNomina = miCliente.buscarCuentaNomina(idCuenta);
            jTNumeroCuenta.setText(miNomina.getNumeroBancario());
            jTMonto.setText(miNomina.getMonto() + "");
            jTNIT.setText(miNomina.getEmpresa().getNIT());

        }

    }
