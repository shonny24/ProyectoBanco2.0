    public void bloquearTxt() {
        jTNIT.setEnabled(false);
        jDateFechaRetiro.setEnabled(false);
        jTMonto.setEnabled(false);

        if (corriente) {
            jTNIT.setEnabled(false);
            jDateFechaRetiro.setEnabled(false);
            jTMonto.setEnabled(true);

            jTNIT.setText("");
        } else if (ahorro) {

            jTNIT.setEnabled(false);
            jTNIT.setText("");
            jDateFechaRetiro.setEnabled(true);
            jTMonto.setEnabled(true);
        } else {
            jTNIT.setEnabled(true);
            jDateFechaRetiro.setEnabled(false);
            jTMonto.setEnabled(true);

            jTMonto.setText("");
        }
    }

