    private void jBIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBIniciarSesionActionPerformed
        // TODO add your handling code here:
        String usuario = jTusuarioLogin.getText();
        char[] obtenerPassword = jPasswordLogin.getPassword();
        String password = "";
        for (int i = 0; i < obtenerPassword.length; i++) {
            password += obtenerPassword[i];
        }
        if (jCheckBoxEmpresa.isSelected()) {
            Empresa empresa = miBanco.buscarEmpresaPorUsuario(usuario);
            if (empresa != null&& empresa.getPassword().equals(password)) {
                VentanaPrincipal.jLabelUsuarioSesion.setVisible(true);
                VentanaPrincipal.jLabelUsuarioSesion.setText(empresa.getUsuario());
                jMenuLogin.setVisible(false);
                jMenuCuenta.setVisible(true);

                VentanaPrincipal.bandera = true;
                //llamamos al metodo
                permisos(empresa.getPermisos().name());
                //prueba de mandar el objeto de sesion
                VentanaPrincipal.empresaSesion = empresa;
                setVisible(false);

            } else {
                JOptionPane.showMessageDialog(null, java.util.ResourceBundle.getBundle("properties/VentanaLogin").getString("LA EMPRESA NO EXISTE"));
            }
        } else {
            Cliente cliente = miBanco.buscarClientePorUsuario(usuario);
            if (cliente != null && cliente.getPassword().equals(password)) {
                
                VentanaPrincipal.jLabelUsuarioSesion.setVisible(true);
                VentanaPrincipal.jLabelUsuarioSesion.setText(cliente.getUsuario());
                
                jMenuLogin.setVisible(false);
                jMenuCuenta.setVisible(true);
                VentanaPrincipal.bandera = false;
                
                //llamamos al metodo
                permisos(cliente.getPermisos().name());

                //prueba de mandar el objeto de sesion
                VentanaPrincipal.clienteSesion = cliente;
                setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, java.util.ResourceBundle.getBundle("properties/VentanaLogin").getString("EL CLIENTE NO EXISTE"));
            }
        }

    }//GEN-LAST:event_jBIniciarSesionActionPerformed

