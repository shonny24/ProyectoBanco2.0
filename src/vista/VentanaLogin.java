    public void permisos(String permiso) {
        jMenuCuenta.setVisible(true);
        if ("CLIENTE".equals(permiso)) {
            jMenuUsuarioAdmin.setVisible(false);
            jMenuUsuarioEmpresa.setVisible(false);
        }else if("EMPRESA".equals(permiso)){
            jMenuUsuarioAdmin.setVisible(false);
            jMenuIUsuario.setVisible(false);
        }else{
            jMenuUsuarioEmpresa.setVisible(false);
            jMenuIUsuario.setVisible(false);
        }
    }

